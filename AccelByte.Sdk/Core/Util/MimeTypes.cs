        public string ConvertFromFilename(string fileName, string defaultType)
        {
            string? ext = Path.GetExtension(fileName).ToLower();
            if (ext == null)
                return defaultType;
            else
            {
                if (_Types.ContainsKey(ext))
                    return _Types[ext];
                else
                    return defaultType;
            }
        }
